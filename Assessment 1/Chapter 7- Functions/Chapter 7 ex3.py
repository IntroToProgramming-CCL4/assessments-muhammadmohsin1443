# Defining a function called make_shirt
def make_shirt(size, message):
    """Prints a sentence summarizing the size and message of a shirt."""
    print(f"Made a shirt of size {size.upper()} with the message: '{message}'.")

# Calling the function using positional arguments
make_shirt('medium', 'LETS PLAY FOOTBALLLLL!!!!')

# Calling the function using keyword arguments
make_shirt(size='large', message='GOOOAAAAALLLLLLLLL!!!!!!!!!!!!!1!')
